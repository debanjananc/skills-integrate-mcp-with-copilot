# Initialize DB and migrate initial activities if needed
data_dir = current_dir.parent / "data"
data_dir.mkdir(exist_ok=True)
database_path = os.environ.get("DATABASE_URL", str(data_dir / "db.sqlite"))
db.init_db(database_path)
# If there are no activities in the DB yet, migrate from the initial blob
if not db.get_activities():
    db.import_activities(INITIAL_ACTIVITIES)


@app.get("/")
def root():
    return RedirectResponse(url="/static/index.html")


@app.get("/activities")
def get_activities():
    return db.get_activities()


@app.post("/activities/{activity_name}/signup")
def signup_for_activity(activity_name: str, email: str):
    """Sign up a student for an activity (persisted)"""
    try:
        db.add_participant(activity_name, email)
    except ValueError as e:
        raise HTTPException(status_code=400, detail=str(e))
    except LookupError:
        raise HTTPException(status_code=404, detail="Activity not found")

    return {"message": f"Signed up {email} for {activity_name}"}


@app.delete("/activities/{activity_name}/unregister")
def unregister_from_activity(activity_name: str, email: str):
    """Unregister a student from an activity (persisted)"""
    try:
        db.remove_participant(activity_name, email)
    except ValueError as e:
        raise HTTPException(status_code=400, detail=str(e))
    except LookupError:
        raise HTTPException(status_code=404, detail="Activity not found")

    return {"message": f"Unregistered {email} from {activity_name}"}
